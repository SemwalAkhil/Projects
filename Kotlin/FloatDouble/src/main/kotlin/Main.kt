fun main() {
    val minFloatValue = Short.MIN_VALUE
    val maxFloatValue = Short.MAX_VALUE
    println("Float max : $maxFloatValue")
    println("Float min : $minFloatValue")

    val minDoubleValue = Double.MIN_VALUE
    val maxDoubleValue = Double.MAX_VALUE
    println("Double max : $maxDoubleValue")
    println("Double min : $minDoubleValue")

    val tempFloat :Float = 2f
    val tempDouble:Double = 2.0
    println("TempFloat : $tempFloat")
    println("TempDouble : $tempDouble")
}